let x: number = 10;
let y: number = 20;

// Simple if statement
if (x > 5) {
    console.log("x is greater than 5");
}

// If-else statement
if (x > y) {
    console.log("x is greater than y");
} else {
    console.log("x is not greater than y");
}

// If-else-if chain
if (x === y) {
    console.log("x equals y");
} else if (x < y) {
    console.log("x is less than y");
} else {
    console.log("x is greater than y");
}